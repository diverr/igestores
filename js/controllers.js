function CalendarCtrl($scope) {
    var date = new Date();
    var d = date.getDate();
    var m = date.getMonth();
    var y = date.getFullYear();

    // Events
    $scope.events = [
        {title: 'Probando',start: new Date(y, m, 1)},
        {title: 'Hola amigos',start: new Date(y, m, d - 5),end: new Date(y, m, d - 2)},
        {id: 999,title: 'Evento periódico',start: new Date(y, m, d - 3, 16, 0),allDay: false},
        {id: 999,title: 'Evento periódico',start: new Date(y, m, d + 4, 16, 0),allDay: false},
        {title: 'Cumpleaños',start: new Date(y, m, d + 1, 19, 0),end: new Date(y, m, d + 1, 22, 30),allDay: false},
        {title: 'elmundo.es',start: new Date(y, m, 28),end: new Date(y, m, 29),url: 'http://www.elmundo.es'}
    ];


    /* message on eventClick */
    $scope.alertOnEventClick = function( event, allDay, jsEvent, view ){
        $scope.alertMessage = (event.title + ': Clicked ');
    };
    /* message on Drop */
    $scope.alertOnDrop = function(event, dayDelta, minuteDelta, allDay, revertFunc, jsEvent, ui, view){
        $scope.alertMessage = (event.title +': Droped to make dayDelta ' + dayDelta);
    };
    /* message on Resize */
    $scope.alertOnResize = function(event, dayDelta, minuteDelta, revertFunc, jsEvent, ui, view ){
        $scope.alertMessage = (event.title +': Resized to make dayDelta ' + minuteDelta);
    };

    /* config object */
    $scope.uiConfig = {
        calendar:{
            height: 450,
            editable: true,
            header: {
                left: 'prev,next',
                center: 'title',
                right: 'month,agendaWeek,agendaDay'
            },
            eventClick: $scope.alertOnEventClick,
            eventDrop: $scope.alertOnDrop,
            eventResize: $scope.alertOnResize,
            lang: 'es'
        }
    };

    /* Event sources array */
    $scope.eventSources = [$scope.events];
}

function MeetingsCtrl($scope, $timeout, Meetings) {
    $scope.items = [];

    Meetings.getAll().then(function(data) {
        $scope.items = data;
    });

    $timeout(function(){
        $('.table').trigger('footable_redraw');
    }, 100);
}

function ChatsCtrl($scope, $timeout, Chats) {
    $scope.items = Chats.getAll();
    

    $timeout(function(){
        $('.table').trigger('footable_redraw');
    }, 100);
}

